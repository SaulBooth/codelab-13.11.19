#include <iostream>
using namespace std;

int main()
{
	//1
	for (int i = 0; i <= 50; i++) {
		cout << i << endl;
	}
	cout << endl;

	//2
	for (int i = 50; i >= 0; i--) {
		cout << i << endl;
	}
	cout << endl;

	//3
	for (int i = 30; i <= 50; i++) {
		cout << i << endl;
	}
	cout << endl;

	//4
	for (int i = 50; i >= 10; i--) {
		cout << i << endl;
	}
	cout << endl;

	//5
	for (int i = 100; i <= 200; i=i+5) {
		cout << i << endl;
	}
	cout << endl;

	return 0;
}